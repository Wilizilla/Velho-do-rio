# Operadores de comparação (relacionais)

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 1 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'

print(f'comparador maior é {maior}')
print(f'comparador maior ou igual é {maior_ou_igual}')
print(f'comparador menor é {menor}')
print(f'comparador menor ou igual é {menor_ou_igual}')
print(f'comparador igual é {igual}')
print(f'comparador diferente é {diferente}')